#include "memory.h"
#include <stdlib.h>

/////////////////////////////////////////////////////////////////////////////////
/// Zero out an object.
///
/// Input: ___obj - the object to zero out
/////////////////////////////////////////////////////////////////////////////////
#define ZERO_OBJECT(__obj) memset(&(__obj), 0, sizeof((__obj)))

/////////////////////////////////////////////////////////////////////////////////
/// Get the minimum of two values inputed
///
/// Input: a, b - The two values to test
/////////////////////////////////////////////////////////////////////////////////

#define MIN(a, b)  (((a) < (b)) ? (a) : (b))

/////////////////////////////////////////////////////////////////////////////////
/// Get the maximum of two values inputed
///
/// Input: a, b - The two values to test
/////////////////////////////////////////////////////////////////////////////////

#define MAX(a, b)  (((a) > (b)) ? (b) : (a))

/////////////////////////////////////////////////////////////////////////////////
/// Get a random float value
///
/// Input: 
/////////////////////////////////////////////////////////////////////////////////

#define RANDOM_FLOAT (((float)rand()-(float)rand()) / RAND_MAX)

/////////////////////////////////////////////////////////////////////////////////
/// Test if number is inside min/max range
///
/// Input: x   - The number to test
///        min - Min value
///        max - max value
/////////////////////////////////////////////////////////////////////////////////

#define CHECK_RANGE(x,min,max) ((x= (x<min  ? min : x<max ? x : max)))

/////////////////////////////////////////////////////////////////////////////////
/// Convert from radians to degrees
///
/// Input: angle - number to convert
/////////////////////////////////////////////////////////////////////////////////

#define DEG_TO_RAD(angle)  ((angle)*PI/180)

/////////////////////////////////////////////////////////////////////////////////
/// Convert from degrees to radians
///
/// Input: angle - number to convert
/////////////////////////////////////////////////////////////////////////////////

#define RAD_TO_DEG(radians) ((radians)*180/PI)

/////////////////////////////////////////////////////////////////////////////////
/// Obtain the square root of a number
///
/// Input: number - the number to square
/////////////////////////////////////////////////////////////////////////////////

#define SQUARE(number) (number*number)

#define RAND_RANGE(x,y) ( (x) + (rand()%((y)-(x)+1)))

